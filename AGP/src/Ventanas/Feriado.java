    public void mostrar_registros()
    {
        try
        {
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.setColumnIdentifiers(new Object[]{"Id Feriado","Feriado","Descripcion"});
            tTabla.setModel(dtm);
            editar_forma_tabla();
            FeriadoDAO dao = new FeriadoDAO();
            ArrayList<Modelo.DTO.Feriado> dato;
            dato = dao.obtener_feriados();
            for(int i=0; i<dato.size(); i++)
            {
                dtm.addRow(new Object[]{dato.get(i).getIdFeriado(), //LLENAMOS LA TABLA, ID
                                        dato.get(i).getFeriado(), //TIPO
                                        dato.get(i).getDescripcion()}); //DESCRIPCION
            }
        }catch(Exception ex)
        {
            
        }
    }
    
    public void llenar_tabla() //PARA LLENAR EL FORMULARIO A PARTIR DE LA TABLA
    {
        try
        {
            int indice = tTabla.getSelectedRow(); //LA FILA QUE SELECCIONAMOS
            int id_feriado = (int) tTabla.getValueAt(indice,0); //OBTENEMOS EL ID DEL REGISTRO
            FeriadoDAO dao = new FeriadoDAO(); //OBJETO TIPO DAO
            ArrayList<Modelo.DTO.Feriado> lista = dao.buscar_feriado(id_feriado); //ARRAT DE TIPO
            txtId.setText(lista.get(0).getIdFeriado()+""); //NO OLVIDAR QUE DEBEMOS RESCATAR EL ID
            cFeriado.setDate(lista.get(0).getFeriado());
            txtDescripcion.setText(lista.get(0).getDescripcion()); //CARGAMOS EL TXT DESCRIPCION
        }catch(Exception ex)
        {
            
        }
    }
    
    public void editar_forma_tabla() //PARA OCULTAR LA COLUMNA ID DE LA TABLA
    {
        tTabla.getColumnModel().getColumn(0).setMaxWidth(0); //LARGO MAXIMO
        tTabla.getColumnModel().getColumn(0).setMinWidth(0); //MINIMO DE LARGO
        tTabla.getColumnModel().getColumn(0).setPreferredWidth(0); //PARA BLOQUEAR EL MODIFICAR EL LARGO DE LA TABLA
    }
    
    public void limpiar()
    {
        txtId.setText("");
        txtDescripcion.setText("");
        cFeriado.setDate(null);
    }

