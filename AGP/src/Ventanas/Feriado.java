    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
    try
    {
        if(cFeriado.getDate()==null)
        {
            JOptionPane.showMessageDialog(null,"Ingrese fecha del feriado"); //GATILLA MENSAJE DE ERROR
            cFeriado.requestFocus(); //LA APLICACION FOCUSEA EL CAMPO DE ENTRADA
            throw new SQLException(); //SE EJECUTA UNA EXCEPCION, POR ENDE SE DETIENE EN ESTE PUNTO
        }
        if(txtDescripcion.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Ingrese descripcion junto con ley asociada"); //GATILLA MENSAJE DE ERROR
            txtDescripcion.requestFocus(); //LA APLICACION FOCUSEA EL CAMPO DE ENTRADA
            throw new SQLException(); //SE EJECUTA UNA EXCEPCION, POR ENDE SE DETIENE EN ESTE PUNTO
        }
        if(txtDescripcion.getText().length()>=256)
        {
            JOptionPane.showMessageDialog(null,"Maximo 255 Caracteres para la descripcion"); //GATILLA MENSAJE DE ERROR
            txtDescripcion.requestFocus(); //LA APLICACION FOCUSEA EL CAMPO DE ENTRADA
            throw new SQLException(); //SE EJECUTA UNA EXCEPCION, POR ENDE SE DETIENE EN ESTE PUNTO
        }
        FeriadoDAO dao = new FeriadoDAO();
        Modelo.DTO.Feriado f = new Modelo.DTO.Feriado();
        f.setFeriado(new java.sql.Date(cFeriado.getDate().getTime()));
        f.setDescripcion(txtDescripcion.getText());
        
        //REGISTRAMOS EL NUEVO FERIADO
            
        JOptionPane.showMessageDialog(null,dao.registrar_feriado(f).substring(0,29));
        
        //LIMPIAR EL FORMULARIO

        limpiar();
        mostrar_registros();
        cFeriado.requestFocus();
              
       
    }catch(Exception ex)
    {
        JOptionPane.showMessageDialog(null,"Problemas al registrar el Feriado, compruebe que no este repetido"); //POR RESOLVER
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

